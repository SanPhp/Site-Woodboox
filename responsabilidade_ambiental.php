     	<h3>Responsabilidade Ambiental.</h3>
        
        <h2>O que é responsabilidade ambiental?</h2>
        <div style="clear:both"></div>
        
        <p>Responsabilidade Ambiental é um conjunto de atitudes, individuais ou empresarias, voltado para o           desenvolvimento sustentável do planeta. Ou seja, estas atitudes devem levar em conta o crescimento           econômico ajustado à proteção do meio ambiente na atualidade e para as gerações futuras, garantindo a           sustentabilidade.</p>
        
        <h4>Exemplos de atitudes que envolvem a responsabilidade ambiental individual:</h4>
        
        <p>- Realizar a reciclagem de lixo (resíduos sólidos).
		   - Não jogar óleo de cozinha no sistema de esgoto.<br>

           - Usar de forma racional, economizando sempre que possível, a água.<br>

           - Buscar consumir produtos com certificação ambiental e de empresas que respeitem o meio ambiente em             seus processos produtivos.<br>

           - Usar transporte individual (carros e motos) só quando necessário, dando prioridades para o             transporte coletivo ou bicicleta.<br>

           - Comprar e usar eletrodomésticos com baixo consumo de energia.<br>

           - Economizar energia elétrica nas tarefas domésticas cotidianas.<br>

           - Evitar o uso de sacolas plásticas nos supermercados.</p>
           
           <h4>Exemplos de atitudes que envolvem a responsabilidade ambiental empresarial:</h4>
           
           <p style="margin: 0px 0px 110px 0px;">- Criação e implantação de um sistema de gestão ambiental na empresa.<br>

              - Tratar e reutilizar a água dentro do processo produtivo.<br>

              - Criação de produtos que provoquem o mínimo possível de impacto ambiental.<br>

              - Dar prioridade para o uso de sistemas de transporte não poluentes ou com baixo índice de                poluição. Exemplos: transporte ferroviário e marítimo.<br>

              - Criar sistema de reciclagem de resíduos sólidos dentro da empresa.<br>

              - Treinar e informar os funcionários sobre a importância da sustentabilidade.<br>

              - Dar preferência para a compra de matéria-prima de empresas que também sigam os princípios da                responsabilidade ambiental.<br>

              - Dar preferência, sempre que possível, para o uso de fontes de energia limpas e renováveis no                processo produtivo.<br>

              - Nunca adotar ações que possam provocar danos ao meio ambiente como, por exemplo, poluição de                rios e desmatamento.
</p>
     
